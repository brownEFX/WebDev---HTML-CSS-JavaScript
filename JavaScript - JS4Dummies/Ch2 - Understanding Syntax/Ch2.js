//Printing a String
let userName = "Kabo" 
console.log("Hello, world. My name is " + userName);

//escaping the quotation mark - using backslash before a quotation mark in a string.
console.log("Hi, " + userName + ". Joe said, 'Hi.'");
console.log("Hi, " + userName + '. Joe said, "Hi."');
console.log("Hi, " + userName + ". Joe said, \"Hi.\"");
//Printing the backslash:
console.log("Hi, " + userName + ". Joe said, \\\"Hi.\"");